export class Empleado{

    nombre : String = "";
    apellido : String = "";
    cargo : String = "";
    salario : number = 0;

    constructor(nombre : String, apellido : String, cargo : String, salario : number){
        this.nombre = nombre;
        this.apellido = apellido;
        this.cargo = cargo;
        this.salario = salario;
    }

    


}